interface ExamplesPanelProps {
  onSelect: (code: string) => void;
  activeCategory: string;
  onCategoryChange: (cat: string) => void;
}

const categories = ['الكل', 'أساسي', 'خوارزميات', 'ذكاء اصطناعي'];

export default function ExamplesPanel({ onSelect, activeCategory, onCategoryChange }: ExamplesPanelProps) {
  const filtered = activeCategory === 'الكل'
    ? examples
    : examples.filter(e => e.category === activeCategory);

  return (
    <div className="examples-panel">
      <div className="examples-header">
        <h3>أمثلة</h3>
        <div className="cat-tabs">
          {categories.map(cat => (
            <button
              key={cat}
              className={`cat-tab ${activeCategory === cat ? 'active' : ''}`}
              onClick={() => onCategoryChange(cat)}
            >
              {cat}
            </button>
          ))}
        </div>
      </div>
      <div className="examples-list">
        {filtered.map((ex, i) => (
          <button
            key={i}
            className="example-item"
            onClick={() => onSelect(ex.code)}
          >
            <span className={`example-badge badge-${ex.category === 'ذكاء اصطناعي' ? 'ai' : ex.category === 'خوارزميات' ? 'algo' : 'basic'}`}>
              {ex.category}
            </span>
            <span className="example-title">{ex.title}</span>
            <span className="example-desc">{ex.description}</span>
          </button>
        ))}
      </div>
    </div>
  );
}
