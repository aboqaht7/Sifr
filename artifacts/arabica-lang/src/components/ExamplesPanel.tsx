const CATEGORIES: Example['category'][] = ['أساسي', 'خوارزميات', 'ذكاء اصطناعي'];

const CATEGORY_COLORS: Record<Example['category'], string> = {
  'أساسي': 'bg-blue-500/20 text-blue-300',
  'خوارزميات': 'bg-purple-500/20 text-purple-300',
  'ذكاء اصطناعي': 'bg-emerald-500/20 text-emerald-300',
};

interface Props {
  onSelect: (code: string) => void;
}

export default function ExamplesPanel({ onSelect }: Props) {
  const [filter, setFilter] = useState<Example['category'] | 'الكل'>('الكل');
  const filtered = filter === 'الكل' ? EXAMPLES : EXAMPLES.filter((e) => e.category === filter);

  return (
    <div className="flex flex-col h-full">
      <div className="px-4 py-3 border-b border-white/5">
        <h2 className="text-sm font-semibold text-amber-400 mb-3">أمثلة</h2>
        <div className="flex flex-wrap gap-1.5">
          <button
            onClick={() => setFilter('الكل')}
            className={`text-xs px-2.5 py-1 rounded-md transition ${
              filter === 'الكل' ? 'bg-amber-500 text-slate-900 font-semibold' : 'bg-white/5 text-slate-300 hover:bg-white/10'
            }`}
          >
            الكل
          </button>
          {CATEGORIES.map((cat) => (
            <button
              key={cat}
              onClick={() => setFilter(cat)}
              className={`text-xs px-2.5 py-1 rounded-md transition ${
                filter === cat ? 'bg-amber-500 text-slate-900 font-semibold' : 'bg-white/5 text-slate-300 hover:bg-white/10'
              }`}
            >
              {cat}
            </button>
          ))}
        </div>
      </div>

      <div className="flex-1 overflow-y-auto px-2 py-2 space-y-1">
        {filtered.map((ex) => (
          <button
            key={ex.id}
            onClick={() => onSelect(ex.code)}
            className="w-full text-right px-3 py-2.5 rounded-lg hover:bg-white/5 transition group border border-transparent hover:border-white/5"
          >
            <div className={`text-[10px] inline-block px-1.5 py-0.5 rounded mb-1 ${CATEGORY_COLORS[ex.category]}`}>
              {ex.category}
            </div>
            <div className="text-sm font-semibold text-slate-100 group-hover:text-amber-300">{ex.title}</div>
            <div className="text-xs text-slate-400 mt-0.5">{ex.description}</div>
          </button>
        ))}
      </div>
    </div>
  );
}
